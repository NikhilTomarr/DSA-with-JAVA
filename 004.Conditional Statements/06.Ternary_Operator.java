// #Ternary Operator:
// variable = (condition) ? statement1 : statement2;

// For example:
public class JavaBasics{
    public static void main(String[] args) {
        int number = 7;
        String result = (number % 2 == 0) ? "Even" : "Odd";
        System.out.println("The number is " + result);
    }
}
