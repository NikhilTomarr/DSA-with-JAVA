//#For Loop: A for loop in Java is a control flow statement that repeatedly executes a block of code a known number of times. It provides a concise way to manage the loop's initialization, condition checking, and iteration step in a single line. 
// Syntax:
// for (initialization; condition; updation) {
//     // code block to be executed repeatedly
// }

//For Loop Example:
public  class JavaBasics{
    public static void main (String[] args){
        for(int i=1; i<=5; i++){
            System.out.println("Hello World");
        }
    }
}
